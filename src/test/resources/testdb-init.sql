CREATE TABLE  PERSONS (
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
   PERSON_NAME VARCHAR(255),
   PHONE_NUMBER VARCHAR(255),
   ADDRESS VARCHAR(255)
 );

CREATE TABLE rooms
(
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
  capacity INT,
  ROOM_NUMBER INT,
  pricePerDay DECIMAL
);

 CREATE TABLE rents
(
   ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
   price DECIMAL,
   roomId BIGINT ,
   personId BIGINT,
   startDay DATE,
   expectedEndDay DATE,
   realEndDay TIMESTAMP,
   countOGuestsInRoom INT
);